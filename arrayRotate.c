#include <stdio.h>
void leftRotateByOne(int arr[], int n) {
    int temp = arr[0];
    for (int i=1; i<n; i++) {
        arr[i-1] = arr[i];
    }
    arr[n-1] = temp;
}


void leftRotate(int arr[], int n, int rB) {
    for (int i=0; i<rB; i++) {
        leftRotateByOne(arr, n);
    }
}


int main() {
    int arr[] = {2,3,4,5,6};
    int n = sizeof(arr)/sizeof(arr[0]);

    leftRotate(arr,n,3);

    for(int i=0; i<n; i++) {
        printf("%d ", arr[i]);
    }
    return 0;
}
